import UIKit

extension UIFont {
    var titleFont: UIFont? {
        return UIFont.systemFont(ofSize: 24)
    }
    
    var subtitleFont: UIFont? {
        return UIFont.systemFont(ofSize: 24)
    }
    
    var mainFont: UIFont? {
        return UIFont.systemFont(ofSize: 22)
    }
}
